def _dedup(seq) -> list[str]:
    """순서 보존 중복 제거."""
    seen: set[str] = set()
    out = []
    for x in seq:
        if x not in seen:
            seen.add(x)
            out.append(x)
    return out


def for_sector(sector: str | None) -> EventKeywords:
    """공통 + 섹터별 키워드를 병합한 이벤트 검색 키워드. 섹터 미상이면 공통만."""
    sec_cat, sec_risk = _SECTOR.get(sector or "", ((), ()))
    return EventKeywords(
        sector=sector,
        catalysts=_dedup((*sec_cat, *_COMMON_CATALYSTS)),  # 섹터 특화를 앞에(우선순위)
        risks=_dedup((*sec_risk, *_COMMON_RISKS)),
        disclosure_filters=list(_COMMON_DISCLOSURE),
    )


def search_queries(stock_name: str, kw: EventKeywords, per_side: int = 6) -> list[str]:
    """뉴스 검색용 쿼리 생성: '{종목명} {키워드}'. 촉매·리스크 각 상위 per_side 개.

    섹터 특화 키워드가 앞에 오므로 per_side 로 자르면 해당 섹터에 유의미한 것부터 쓴다.
    """
    picked = kw.catalysts[:per_side] + kw.risks[:per_side]
    return [f"{stock_name} {k}" for k in _dedup(picked)]
